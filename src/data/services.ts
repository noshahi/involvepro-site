function svc(input: {
  title: string;
  slug: string;
  group: ServiceGroupKey;
  headline: string;
  shortDescription: string;
  heroVisualType: Service["heroVisualType"];
  problemTitle: string;
  problemDescription: string;
  whatWeDo: string[];
  deliverables: string[];
  bestFor: string[];
  faqs: ServiceFAQ[];
  relatedServices: string[];
  secondaryCTA?: string;
  metaTitle?: string;
  metaDescription?: string;
  icon: string;
  accent: Service["accent"];
}): Service {
  const groupLabel = serviceGroups.find((g) => g.key === input.group)?.label ?? input.group;
  return {
    title: input.title,
    slug: input.slug,
    group: input.group,
    eyebrow: groupLabel,
    headline: input.headline,
    shortDescription: input.shortDescription,
    metaTitle: input.metaTitle ?? `${input.title} | Involvepro`,
    metaDescription: input.metaDescription ?? input.shortDescription,
    heroVisualType: input.heroVisualType,
    problemTitle: input.problemTitle,
    problemDescription: input.problemDescription,
    whatWeDo: input.whatWeDo,
    deliverables: input.deliverables,
    process: sharedProcess,
    bestFor: input.bestFor,
    faqs: input.faqs,
    relatedServices: input.relatedServices,
    primaryCTA: "Schedule a Meeting",
    secondaryCTA: input.secondaryCTA ?? "View Related Services",
    schemaDescription: input.shortDescription,
    icon: input.icon,
    accent: input.accent,
  };
}

